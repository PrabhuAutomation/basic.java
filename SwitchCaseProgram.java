package module1;

public class SwitchCaseProgram {

	public static void main(String[] args) 
	{
            switch (8)
            {
            case 1:
            	
            	System.out.println("withdraw money");
            	break;
            	
            	case 2:
            		System.out.println("contribute money");
                	break;
            		
            	case 3:
            		System.out.println("change the pin");
                	break;
            		
            		
            	case 4:
            		System.out.println("change the language");
                	break;
            		
            		default :
            			System.out.println("selection is wrong");
            			        		
            }


	}

}
